class UserSocialProfileOwnPhotosCollectionViewController: UICollectionViewController, UICollectionViewDelegateFlowLayout {

    var event : Event?
    var eventPhotos : [Social] = []
    
    override func viewDidLoad() {
        super.viewDidLoad()

